export type Rank = 'B' | 'A' | 'S' | 'SS' | 'Lv1' | 'Lv2' | 'Lv3';
export type MarkType = 'elite' | 'regular';
export type Expansion = 'ARR' | 'HW' | 'SB' | 'ShB' | 'EW' | 'DT';

export interface HuntMark {
  id: string;
  name: string;
  rank: Rank;
  type: MarkType;
  expansion: Expansion;
  zone: string;
  region: string;
  billCount?: number;
}

const slug = (s: string) =>
  s.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, '');

function elite(name: string, rank: 'B' | 'A' | 'S' | 'SS', expansion: Expansion, zone: string, region: string): HuntMark {
  return { id: `${slug(expansion)}-${slug(zone)}-${slug(rank)}-${slug(name)}`, name, rank, type: 'elite', expansion, zone, region };
}

function reg(name: string, rank: 'Lv1' | 'Lv2' | 'Lv3', expansion: Expansion, zone: string, region: string): HuntMark {
  const billCount = rank === 'Lv1' ? 5 : rank === 'Lv2' ? 3 : 2;
  return { id: `${slug(expansion)}-${slug(zone)}-${slug(rank)}-${slug(name)}`, name, rank, type: 'regular', expansion, zone, region, billCount };
}

